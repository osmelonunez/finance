def get_lang():
    lang = (session.get("lang") or "").strip().lower()
    if lang in SUPPORTED_LANGS:
        return lang
    best = request.accept_languages.best_match(SUPPORTED_LANGS)
    return best or "en"


def t(text: str, lang: str | None = None) -> str:
    language = lang or get_lang()
    return TRANSLATIONS.get(language, {}).get(text, text)


def category_name(name: str | None, lang: str | None = None) -> str:
    if not name:
        return ""
    language = lang or get_lang()
    if language != "es":
        return name
    cfg = DEFAULT_CATEGORY_I18N.get(name)
    return (cfg or {}).get("es_name", name)


def category_description(name: str | None, description: str | None, lang: str | None = None) -> str:
    language = lang or get_lang()
    if language != "es":
        return description or ""
    cfg = DEFAULT_CATEGORY_I18N.get(name or "")
    if cfg and cfg.get("es_description"):
        return cfg["es_description"]
    return description or ""
